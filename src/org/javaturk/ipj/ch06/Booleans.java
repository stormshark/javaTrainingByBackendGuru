package org.javaturk.ipj.ch06;

public class Booleans {

    public static void main(String[] args) {
        boolean b1 = true;
        System.out.println(b1);

        boolean b2 = false;
        System.out.println(b2);

        boolean b3;
        b3 = true;
        System.out.println(b3);
    }
}
